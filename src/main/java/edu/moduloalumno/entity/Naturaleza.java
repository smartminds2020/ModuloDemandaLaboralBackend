package edu.moduloalumno.entity;

public class Naturaleza {
	Integer id_naturaleza;
	String naturaleza_desc;
	String naturaleza_desc_min;
	
	public Integer getId_naturaleza() {
		return id_naturaleza;
	}
	public void setId_naturaleza(Integer id_naturaleza) {
		this.id_naturaleza = id_naturaleza;
	}
	public String getNaturaleza_desc() {
		return naturaleza_desc;
	}
	public void setNaturaleza_desc(String naturaleza_desc) {
		this.naturaleza_desc = naturaleza_desc;
	}
	public String getNaturaleza_desc_min() {
		return naturaleza_desc_min;
	}
	public void setNaturaleza_desc_min(String naturaleza_desc_min) {
		this.naturaleza_desc_min = naturaleza_desc_min;
	}
	
}
